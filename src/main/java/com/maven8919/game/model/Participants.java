package com.maven8919.game.model;

public class Participants {

	private Long id;
	private String femalePlayerName;
	private String malePlayerName;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getFemalePlayerName() {
		return femalePlayerName;
	}

	public void setFemalePlayerName(String femalePlayerName) {
		this.femalePlayerName = femalePlayerName;
	}

	public String getMalePlayerName() {
		return malePlayerName;
	}

	public void setMalePlayerName(String malePlayerName) {
		this.malePlayerName = malePlayerName;
	}

}
